													<h2>About the Indian Journal of Medical Ethics</h2>
													<p>
														The Indian Journal of Medical Ethics, published since 1993 by the <b>Forum for Medical Ethics Society,</b> is a multi-disciplinary academic journal providing a platform for publication of original material and discussion on all aspects of healthcare ethics and the humanities, relevant to and/or from the perspective of India and other developing countries.
													</p>
													<p>
														<b>Multi-disciplinary inquiry:</b> The journal promotes multi-disciplinary and multi-cultural inquiry by academics as well as activists from the fields of bio-medicine, social sciences, law and humanities. It strives to involve all cadres of, and beneficiaries from, the health care system, and strengthen the hands of those with ethical values and concern for the under-privileged.
													</p>
													<p>
														<b>Peer-reviewed and Indexed material:</b> All material submitted for publication in the journal, go through a rigorous system of internal and external peer review by experts, and revisions when needed,  before receiving approval for publication. In 2005, the US National Libraries of Medicine (PubMed) decided to index all previous issues (starting from the first issue in 1993) and also start prospective indexing of IJME. In addition to PubMed, IJME is also indexed in a few other international databases, such as SCOPUS.
													</p>
													<p>
														<b>Free publication and open access:</b> As a policy, since its inception, the IJME has never charged authors for publication of their writings, and all material available on the website of the journal is free and offers open access to all. However, the print copy of the journal is charged for. Those interested in receiving a print copy of the journal may do so on payment of a subscription. The journal does not have institutional support and funding, and is run by a group of volunteers and part-time paid staff. The journal is primarily sustained by donations and grants received from individuals and philanthropic organisations. We therefore encourage readers to make financial contributions to the journal.
													</p>
													<p>
														<b>Periodicity:</b> The IJME is published four times in a year – January, April, July and October. Some material lined up for publication in a future print issue is published in advance on the journal’s website under the section title, “Online First”. In addition, we are gradually expanding publication of material only on the website.
													</p>
													<p>
														The journal is owned and published by the <b>Forum for Medical Ethics Society (FMES).</b>
													</p>
													<h3>Reproduction or translation of IJME material:</h3>
													<p>
														<b>For non-commercial use:</b> The reproduction of the material published on the website for non-commercial educational purposes is encouraged and is free of charge. When reproducing material published in the print issue of the IJME, please give credit to the journal and the author(s) of the material along with all publication details including IJME's website address. Online reproductions should carry a link to the journal. When such material is reproduced, please inform us at <a href="mail to:ijme.editorial@gmail.com">ijme.editorial@gmail.com</a>
													</p>
													<p>
														<b>For commercial use:</b> IJME charges for any commercial use of material published in the print issues and on its website. The commercial use of its material is strictly prohibited unless charges are paid and authorisation provided by the journal in writing. Such reproduction is also required to provide credit to the journal, author(s) and publication details. Please write to us to obtain information regarding the charges at <a href="mail to:ijme.editorial@gmail.com">ijme.editorial@gmail.com</a>
													</p>

